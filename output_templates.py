# カテゴリ一覧を取得
def get_categories():
    """利用可能なカテゴリの一覧を取得"""
    categories = []
    seen = set()
    for template in OUTPUT_TEMPLATES:
        cat = template['category']
        if cat not in seen:
            categories.append(cat)
            seen.add(cat)
    return categories

# カテゴリ別のテンプレート取得
def get_templates_by_category(category):
    """指定カテゴリのテンプレートを取得"""
    return [t for t in OUTPUT_TEMPLATES if t['category'] == category]

# テンプレート名からテンプレートを取得
def get_template_by_name(name):
    """テンプレート名からテンプレートを取得"""
    for template in OUTPUT_TEMPLATES:
        if template['name'] == name:
            return template
    return None

# すべてのテンプレート名を取得
def get_all_template_names():
    """すべてのテンプレート名のリストを取得"""
    return [t['name'] for t in OUTPUT_TEMPLATES]

# カテゴリ別にグループ化したテンプレート名を取得
def get_categorized_template_names():
    """
    カテゴリ別にグループ化したテンプレート名を取得
    
    Returns:
    --------
    dict : {category: [template_names]}
    """
    result = {}
    for template in OUTPUT_TEMPLATES:
        cat = template['category']
        if cat not in result:
            result[cat] = []
        result[cat].append(template['name'])
    return result
